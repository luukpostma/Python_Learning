# Title functie veranderd eerst letter van een string in een hoofdletter.
name = "luuk postma"
print(name.title())

# Upper functie veranderd alle letter van een string in hoofdletters.
name = "luuk postma"
print(name.upper())

# Lower functie veranderd alle letter van een string in kleine letters.
name = "luuk postma"
print(name.lower())

# Het combineren van strings.
first_name = "luuk"
last_name = "postma"
full_name = first_name + " " + last_name
print(full_name.title())

#Spaties moet je handmatig toevoegen.
print("Hello " + first_name + "!")

